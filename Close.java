
public class Close {
int numLine;
String closeA;
	public Close(int numLine,String closeA ) {
	this.numLine=numLine;
	this.closeA=closeA;
	}
	public int getNumLine() {
		return numLine;
	}
	public void setNumLine(int numLine) {
		this.numLine = numLine;
	}
	public String getCloseA() {
		return closeA;
	}
	public void setCloseA(String closeA) {
		this.closeA = closeA;
	}
	
}
